  function detect() {
    try {
      var saved = localStorage.getItem("ss-lang");
      if (saved && D[saved]) return saved;
    } catch (e) {}
    var prefs = navigator.languages || [navigator.language || "en"];
    for (var i = 0; i < prefs.length; i++) {
      var code = String(prefs[i]).slice(0, 2).toLowerCase();
      if (D[code]) return code;
    }
    return "en";
  }

  function apply(lang) {
    if (!D[lang]) lang = "en";
    var dict = D[lang];
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n")];
      if (v != null) el.textContent = v;
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n-html")];
      if (v != null) el.innerHTML = v;
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n-ph")];
      if (v != null) el.setAttribute("placeholder", v);
    });

    // switcher state
    document.querySelectorAll(".lang__btn .code-current").forEach(function (el) {
      el.textContent = lang.toUpperCase();
    });
    document.querySelectorAll("[data-set-lang]").forEach(function (b) {
      b.classList.toggle("is-current", b.getAttribute("data-set-lang") === lang);
    });

    try { localStorage.setItem("ss-lang", lang); } catch (e) {}
  }

  // wire up switchers
  document.querySelectorAll("[data-set-lang]").forEach(function (b) {
    b.addEventListener("click", function () {
      apply(b.getAttribute("data-set-lang"));
      document.querySelectorAll(".lang.open").forEach(function (l) { l.classList.remove("open"); });
      document.body.classList.remove("menu-open");
    });
  });
  document.querySelectorAll(".lang__btn").forEach(function (btn) {
    btn.addEventListener("click", function (ev) {
      ev.stopPropagation();
      btn.closest(".lang").classList.toggle("open");
    });
  });
  document.addEventListener("click", function () {
    document.querySelectorAll(".lang.open").forEach(function (l) { l.classList.remove("open"); });
  });

  apply(detect());
  window.SS_LANGS = LANGS;
